#Leia 2 valores inteiros e armazene-os nas variáveis A e B. 
#Efetue a soma de A e B atribuindo o seu resultado na variável X. 
#Imprima X conforme exemplo apresentado abaixo. 
#Não apresente mensagem alguma além daquilo que está sendo especificado 
#Imprima a mensagem "X = " (letra X maiúscula) seguido pelo valor da variável X e pelo final de linha.

A = int(input()) #A variável 'A' armazena esse valor
B = int(input()) #A variável 'B' armazena o segundo valor

X = A + B #Atribuo à variável 'X' o valor da soma das variáveis A e B.

print(f'X = {X}') #Imprimo 'X' na tela
